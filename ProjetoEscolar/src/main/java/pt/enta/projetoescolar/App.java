    private void importarDadosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importarDadosButtonActionPerformed
        // TODO add your handling code here:
        try {
            FileInputStream fileInputStream = new FileInputStream("D:\\EDAA\\Cursos.txt");
            ObjectInputStream objectInputStream = new ObjectInputStream(fileInputStream);
            listaCursos = (ArrayList) objectInputStream.readObject();
            objectInputStream.close();
            fileInputStream.close();
            for (Cursos cursos : listaCursos) {
                idCurso = cursos.getIdCurso();
            }
            idCurso++;
        } catch (IOException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
        }

        try {
            FileInputStream fileInputStream = new FileInputStream("D:\\EDAA\\Disciplinas.txt");
            ObjectInputStream objectInputStream = new ObjectInputStream(fileInputStream);
            listaDisciplinas = (ArrayList) objectInputStream.readObject();
            objectInputStream.close();
            fileInputStream.close();
            for (Disciplinas disciplinas : listaDisciplinas) {
                idDisciplina = disciplinas.getIdDisciplina();
            }
            idDisciplina++;
        } catch (IOException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
        }

        try {
            FileInputStream fileInputStream = new FileInputStream("D:\\EDAA\\Alunos.txt");
            ObjectInputStream objectInputStream = new ObjectInputStream(fileInputStream);
            listaAlunos = (ArrayList) objectInputStream.readObject();
            objectInputStream.close();
            fileInputStream.close();
            for (Alunos alunos : listaAlunos) {
                idAluno = alunos.getIdAluno();
            }
            idAluno++;
        } catch (IOException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
        }
        
        try {
            FileInputStream fileInputStream = new FileInputStream("D:\\EDAA\\DisciplinasCurso.txt");
            ObjectInputStream objectInputStream = new ObjectInputStream(fileInputStream);
            listaDisciplinasCurso = (ArrayList) objectInputStream.readObject();
            objectInputStream.close();
            fileInputStream.close();
        } catch (IOException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
        }
    }//GEN-LAST:event_importarDadosButtonActionPerformed

