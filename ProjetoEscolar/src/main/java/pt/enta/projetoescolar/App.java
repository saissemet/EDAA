    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        DisciplinasCurso disciplinasCurso = new DisciplinasCurso();
        
        disciplinasCurso.setIdCurso(Integer.parseInt(jTextField1.getText()));
        disciplinasCurso.setIdDisciplina(Integer.parseInt(jTextField2.getText()));
        
        Cursos cursos = listaCursos.get(Integer.parseInt(jTextField1.getText()) - 1);
        disciplinasCurso.setNomeCurso(cursos.getNomeCurso());
        
        Disciplinas disciplinas = listaDisciplinas.get(Integer.parseInt(jTextField2.getText()) - 1);
        disciplinasCurso.setNomeDisciplina(disciplinas.getNomeDisciplina());
        
        listaDisciplinasCurso.add(disciplinasCurso);
        
        textListarDisciplinas.setText("");
        for (DisciplinasCurso disciplinasCurso1 : listaDisciplinasCurso) {
            jTextArea3.append(disciplinasCurso1.toString());
        }
        
        try {
                FileOutputStream fileOutputStream = new FileOutputStream("D:\\EDAA\\DisciplinasCurso.txt");
                try ( ObjectOutputStream objectOutputStream = new ObjectOutputStream(fileOutputStream)) {
                    objectOutputStream.writeObject(listaDisciplinasCurso);
                    objectOutputStream.close();
                    fileOutputStream.close();
                } catch (IOException ex) {
                    Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton20ActionPerformed

