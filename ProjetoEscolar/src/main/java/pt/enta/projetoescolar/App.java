    private void adicionarAlunoFinalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarAlunoFinalButtonActionPerformed
        // TODO add your handling code here:

        Alunos alunos = new Alunos();
        alunos.setNomeAluno(textNomeAluno.getText());
        alunos.setIdadeAluno(Integer.parseInt(textIdadeAluno.getText()));
        if (femininoButton.isSelected()) {
            alunos.setGeneroAluno("Feminino");
        }
        if (masculinoButton.isSelected()) {
            alunos.setGeneroAluno("Masculino");
        }
        alunos.setPaisAluno(textPaisAluno.getText());
        if (textNomeAluno.getText().isEmpty() || textIdadeAluno.getText().isEmpty() || textPaisAluno.getText().isEmpty()) {
            errorFrame1.pack();
            errorFrame1.setVisible(true);
        } else {
            alunos.setIdAluno(idAluno++);
            textIdAluno.setText(String.valueOf(idAluno));

            try {
                listaAlunos.add(alunos);
                FileOutputStream fileOutputStream = new FileOutputStream("D:\\EDAA\\Alunos.txt");
                try ( ObjectOutputStream objectOutputStream = new ObjectOutputStream(fileOutputStream)) {
                    objectOutputStream.writeObject(listaAlunos);
                    objectOutputStream.close();
                    fileOutputStream.close();
                } catch (IOException ex) {
                    Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
            }
            textNomeAluno.setText("");
            textIdadeAluno.setText("");
            textPaisAluno.setText("");
            masculinoButton.setSelected(false);
            femininoButton.setSelected(false);

        }

    }//GEN-LAST:event_adicionarAlunoFinalButtonActionPerformed

